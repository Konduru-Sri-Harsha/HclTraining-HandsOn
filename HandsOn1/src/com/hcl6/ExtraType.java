package com.hcl6;

public class ExtraType {
	public String name;
	public Long runs;

	public ExtraType() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getRuns() {
		return runs;
	}

	public void setRuns(Long runs) {
		this.runs = runs;
	}

}
